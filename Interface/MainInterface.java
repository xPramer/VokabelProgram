    public void setKanaContentSeite2(int id){
        
        String[] content;
        
        if(kana_Streak_Counter >= 10){
            if(!r_to_k){r_to_k = true;}
            else {r_to_k = false;}
            kana_Streak_Counter = 0;
        }
        if(!r_to_k){
            content = dm.getRandomContent(01);
        } else {
            content = dm.getRandomContent(02);
        }
        
        txfKanaContentSeite2Vorgabe.setText(content[0]);
        
        Random rand = new Random();
        int rand_num = rand.nextInt(3);
        switch(rand_num){
            case 0:
                txfKanaContentSeite2Antwort1.setText(content[1]);
                txfKanaContentSeite2Antwort2.setText(content[2]);
                txfKanaContentSeite2Antwort3.setText(content[3]);
                id_Antwort = 1;
                break;
            
            case 1:
                txfKanaContentSeite2Antwort1.setText(content[3]);
                txfKanaContentSeite2Antwort2.setText(content[1]);
                txfKanaContentSeite2Antwort3.setText(content[2]);
                id_Antwort = 2;
                break;
            
            case 2:
                txfKanaContentSeite2Antwort1.setText(content[2]);
                txfKanaContentSeite2Antwort2.setText(content[3]);
                txfKanaContentSeite2Antwort3.setText(content[1]);
                id_Antwort = 3;
                break;
            
        }
        
        switch(id){
            case 0: 
                showContent(pnlKanaContent, pnlKanaContentSeite2);
                break;
            
            case 1: 
                if(punkte_Richtig*2 > punkte_Falsch && punkte_Richtig > 19){
                    setKanaContentSeite3(0);
                    showContent(pnlKanaContent, pnlKanaContentSeite3);
                    
                }
        }
    }
    
