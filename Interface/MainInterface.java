    private void updateUebersicht() throws Exception{
        dm.laodDBData();
        ArrayList<Vokabel> vocs = dm.getDataVocs();
        
        for(int x = 0; x < 3; x++){
            int counter = 0;
            for(int y = 0; y < vocs.size(); y++){
                switch(x){
                    case 0:
                        if(vocs.get(y).getStreak() >= 4){counter++;}
                        break;
                    case 1:
                        if(vocs.get(y).getStreak() >= 2 && vocs.get(y).getStreak() < 4){counter++;}
                        break;
                    case 2:
                        if(vocs.get(y).getStreak() < 2){counter++;}
                        break;
                }
            }
            
            switch(x){
                case 0:
                    txfUebersichtSehrGut.setText(Integer.toString(counter));
                   break; 
                case 1:
                    txfUebersichtGut.setText(Integer.toString(counter));
                    break;
                case 2:
                    txfUebersichtSchlecht.setText(Integer.toString(counter));
                    break;
            }
        }
        txfUebersichtAnzahlWoerter.setText(Integer.toString(vocs.size()));
    }
    
    public void initUhr(){
                
        Thread th = new Thread() {
            @Override
            public void run(){
                while(true){
                    try {
                        LocalTime myTime = LocalTime.now();
                        DateTimeFormatter myFormat = DateTimeFormatter.ofPattern("HH:mm:ss");
                        String formattedTime = myTime.format(myFormat);
                        txfMainUhr.setText(formattedTime);
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(MainInterface.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }; th.start();
    }
    
